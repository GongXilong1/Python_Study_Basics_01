"""
姓名：小明
年龄：18岁
性别：男
身高：1.75米
体重：75.0千克
"""

# 在python中，定义变量时是不需要指定变量的类型的
# 在运行的时候，python解释器，会根据赋值语句等号右侧的数据自动推导出变量中保存数据的准确类型。
#

name = "小明"  # str表示是一个字符串类型
age = 18  # int表示是一个整数类型
gender = True  # 是  bool表示是一个布尔类型，真True 或者 假False
height = 1.75  # float表示一个浮点数类型，（小数类型）。
weight = 75  # int表示是一个整数类型

print(name)
print(age)
print(gender)
print(height)
print(weight)
